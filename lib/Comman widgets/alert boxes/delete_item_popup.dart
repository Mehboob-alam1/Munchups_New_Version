import 'package:munchups_app/Component/utils/utils.dart';

import '../../Apis/post_apis.dart';

class DeleteItemPopUp extends StatefulWidget {
  String id;
  DeleteItemPopUp({super.key, required this.id});

  @override
  State<DeleteItemPopUp> createState() => _DeleteItemPopUpState();
}

class _DeleteItemPopUpState extends State<DeleteItemPopUp> {
  bool isLoading = false;

  @override
  Widget build(BuildContext context) {
    return AlertDialog(
      shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(20)),
      title: Center(
        child: Text(
          TextStrings.textKey['delet_item']!,
          style: const TextStyle(
              color: DynamicColor.primaryColor, fontWeight: FontWeight.bold),
        ),
      ),
      content: Text(
        TextStrings.textKey['delete_item_sub']!,
        textAlign: TextAlign.center,
      ),
      actions: [
        isLoading == false
            ? Container(
                margin: const EdgeInsets.only(bottom: 10),
                child: Row(
                  mainAxisAlignment: MainAxisAlignment.spaceEvenly,
                  children: [
                    CommanButton(
                        hight: 40.0,
                        width: 60.0,
                        buttonName: TextStrings.textKey['no']!,
                        buttonBGColor: DynamicColor.green,
                        onPressed: () {
                          Navigator.pop(context);
                        },
                        shap: 7),
                    CommanButton(
                        hight: 40.0,
                        width: 60.0,
                        buttonName: TextStrings.textKey['yes']!,
                        buttonBGColor: DynamicColor.primaryColor,
                        onPressed: () {
                          deleteFoodApiCall(context);
                        },
                        shap: 7)
                  ],
                ),
              )
            : const Center(
                child: CircularProgressIndicator(
                  color: DynamicColor.primaryColor,
                ),
              )
      ],
    );
  }

  void deleteFoodApiCall(context) async {
    setState(() {
      isLoading = true;
    });
    try {
      await PostApiServer().deletePostedDemandFoodApi(widget.id).then((value) {
        setState(() {
          isLoading = false;
        });
        Utils().myToast(context, msg: value['msg']);
        if (value['success'] == 'true') {
          PageNavigateScreen().back(context);
        }
      });
    } catch (e) {
      Utils().myToast(context, msg: 'Internal server error occurred');

      log(e.toString());
      setState(() {
        isLoading = false;
      });
      PageNavigateScreen().back(context);
    }
  }
}
